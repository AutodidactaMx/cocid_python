import matplotlib.pyplot as plt

# Datos de ejemplo
x = [1, 2, 3, 4, 5]
y = [2, 4, 6, 8, 10]

# Crear un gráfico de líneas
plt.plot(x, y)

# Agregar etiquetas a los ejes
plt.xlabel('Eje X')
plt.ylabel('Eje Y')

# Agregar título al gráfico
plt.title('Gráfico de ejemplo')

# Mostrar el gráfico
plt.show()
